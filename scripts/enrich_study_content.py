def main():
    data = json.loads(CONTENT.read_text(encoding="utf-8"))
    for chapter in data["chapters"]:
        for section in chapter["sections"]:
            update = SECTION_UPDATES[section["id"]]
            section["summary"] = update["summary"]
            section["concept"] = update["concept"]
            section["memorize"] = update["memorize"]
            section["flashcards"] = [
                fc(section["id"], i, question, answer)
                for i, (question, answer) in enumerate(update["flashcards"], start=1)
            ]
            section["quiz"] = {
                "basic": [
                    q(section["id"], "basic", i, question, options, correct, explanation)
                    for i, (question, options, correct, explanation) in enumerate(update["basic"], start=1)
                ],
                "advanced": [
                    q(section["id"], "advanced", i, question, options, correct, explanation)
                    for i, (question, options, correct, explanation) in enumerate(update["advanced"], start=1)
                ],
            }
    CONTENT.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


if __name__ == "__main__":
    main()
