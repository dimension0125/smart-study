def fc(section_id, n, question, answer):
    return {"id": f"fc-{section_id}-{n}", "question": question, "answer": answer}


def quiz(section_id, level, n, question, options, correct, explanation):
    return {
        "id": f"q-{section_id}-{level}-{n}",
        "question": question,
        "options": options,
        "correctAnswer": correct,
        "explanation": explanation,
    }


def make_section(section_id, title, summary, concept, memorize, cards, basic, advanced):
    return {
        "id": section_id,
        "title": title,
        "summary": summary,
        "concept": concept,
        "memorize": memorize,
        "flashcards": [fc(section_id, i, q, a) for i, (q, a) in enumerate(cards, start=1)],
        "quiz": {
            "basic": [
                quiz(section_id, "basic", i, q, options, correct, exp)
                for i, (q, options, correct, exp) in enumerate(basic, start=1)
            ],
            "advanced": [
                quiz(section_id, "advanced", i, q, options, correct, exp)
                for i, (q, options, correct, exp) in enumerate(advanced, start=1)
            ],
        },
    }


